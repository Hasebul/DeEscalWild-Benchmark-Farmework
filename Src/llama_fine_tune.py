base_path = "/content/drive/MyDrive/deescalation_project/Checkpoints/"
dataset_path = f"{base_path}qwen_finetune_data.jsonl"
output_dir = f"{base_path}llama_32"



from unsloth import FastLanguageModel
import torch
from datasets import load_dataset
from trl import SFTTrainer, SFTConfig
from unsloth.chat_templates import get_chat_template

# --- 1. CONFIGURATION ---
max_seq_length = 2048
dtype = None # Auto-detect (Float16 or Bfloat16)
load_in_4bit = True # 4bit quantization (Crucial for 3B models on small GPUs)

# --- 2. LOAD MODEL ---
print("Loading Llama 3.2 3B...")
model, tokenizer = FastLanguageModel.from_pretrained(
    model_name = "unsloth/Llama-3.2-3B-Instruct", # <--- CHANGE 1: Llama Model
    max_seq_length = max_seq_length,
    dtype = dtype,
    load_in_4bit = load_in_4bit,
)

# --- 3. ADD LoRA ADAPTERS ---
# Llama 3.2 works best when targeting all linear layers
model = FastLanguageModel.get_peft_model(
    model,
    r = 16, # Rank (Keep 16 or 32 for 3B models)
    target_modules = ["q_proj", "k_proj", "v_proj", "o_proj",
                      "gate_proj", "up_proj", "down_proj"],
    lora_alpha = 16,
    lora_dropout = 0,
    bias = "none",
    use_gradient_checkpointing = "unsloth",
    random_state = 3407,
)

# --- 4. PREPARE DATA ---
# Load the same JSONL file you created for Qwen
# dataset = load_dataset("json", data_files=dataset_path, split="train")




# Setup the Llama 3 specific chat template
tokenizer = get_chat_template(
    tokenizer,
    chat_template = "llama-3", # <--- CHANGE 2: Specific Template
    mapping = {"role": "role", "content": "content", "user": "user", "assistant": "assistant"},
)

def formatting_prompts_func(examples):
    convos = examples["messages"]
    texts = [tokenizer.apply_chat_template(convo, tokenize = False, add_generation_prompt = False) for convo in convos]
    return { "text" : texts }

# 1. LOAD & SPLIT DATA
# Load the JSONL file we created
dataset = load_dataset("json", data_files=dataset_path, split="train")

# --- CHANGE 1: Create a Test Split ---
# This reserves 10% of your data for validation/testing
# # Load into HF Dataset
# dataset = Dataset.from_list(dataset) # This line was causing the error
dataset = dataset.map(formatting_prompts_func, batched = True)
dataset = dataset.train_test_split(test_size=0.1)
train_dataset = dataset["train"]
eval_dataset = dataset["test"] # This acts as your validation set



# 2. CONFIGURE TRAINER
trainer = SFTTrainer(
    model = model,
    tokenizer = tokenizer,
    train_dataset = train_dataset,
    eval_dataset = eval_dataset,

    # --- FIX: Add this line ---
    formatting_func = formatting_prompts_func,

    # Tell it to load the 'text' field generated by the function
    dataset_text_field = "text",

    max_seq_length = 2048,
    args = SFTConfig(
        per_device_train_batch_size = 2,
        gradient_accumulation_steps = 4,
        # max_steps = 100,
        # ADD this to train for exactly 1 full pass
        num_train_epochs = 2,
        learning_rate = 2e-4,
        fp16 = not torch.cuda.is_bf16_supported(),
        bf16 = torch.cuda.is_bf16_supported(),
        logging_steps = 1,
        eval_strategy = "steps",
        eval_steps = 10,
        output_dir = output_dir,
    ),
)

# --- 6. TRAIN ---
print("Starting Training...")
trainer_stats = trainer.train()

# --- 7. SAVE ---
print("Saving Model...")
model.save_pretrained("outputs_llama/checkpoint-final")
tokenizer.save_pretrained("outputs_llama/checkpoint-final")

