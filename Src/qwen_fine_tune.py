base_path = "/content/drive/MyDrive/deescalation_project/Checkpoints/"
dataset_path = f"{base_path}qwen_finetune_data.jsonl"
output_dir = f"{base_path}qwen_25"


from unsloth import FastLanguageModel
import torch
from datasets import Dataset

# 1. Configuration
max_seq_length = 2048
dtype = None # Auto detection
load_in_4bit = True # Use 4bit quantization to save memory

# 2. Load Model
model, tokenizer = FastLanguageModel.from_pretrained(
    model_name = "unsloth/Qwen2.5-3B-Instruct",
    max_seq_length = max_seq_length,
    dtype = dtype,
    load_in_4bit = load_in_4bit,
)

# 3. Add LoRA adapters
model = FastLanguageModel.get_peft_model(
    model,
    r = 16, # Rank
    target_modules = ["q_proj", "k_proj", "v_proj", "o_proj",
                      "gate_proj", "up_proj", "down_proj"],
    lora_alpha = 16,
    lora_dropout = 0,
    bias = "none",
    use_gradient_checkpointing = "unsloth",
)

# 4. Prepare Your Custom Data
# (Assuming you processed your txt files into a list of dicts called 'conversations')
# Format: [{"messages": [{"role": "system", ...}, {"role": "user", ...}, {"role": "assistant", ...}]}]

from unsloth.chat_templates import get_chat_template

tokenizer = get_chat_template(
    tokenizer,
    chat_template = "chatml", # Qwen uses ChatML
    mapping = {"role": "role", "content": "content", "user": "user", "assistant": "assistant"}
)

def formatting_prompts_func(examples):
    convos = examples["messages"]
    texts = [tokenizer.apply_chat_template(convo, tokenize = False, add_generation_prompt = False) for convo in convos]
    return { "text" : texts }

from unsloth import FastLanguageModel
from trl import SFTTrainer, SFTConfig
from datasets import load_dataset

# ... (Model Loading Code remains the same) ...

# 1. LOAD & SPLIT DATA
# Load the JSONL file we created
dataset = load_dataset("json", data_files=dataset_path, split="train")

# --- CHANGE 1: Create a Test Split ---
# This reserves 10% of your data for validation/testing
# # Load into HF Dataset
from datasets import Dataset
dataset = Dataset.from_list(dataset)
dataset = dataset.map(formatting_prompts_func, batched = True)
dataset = dataset.train_test_split(test_size=0.1)
train_dataset = dataset["train"]
eval_dataset = dataset["test"] # This acts as your validation set

# ... (Formatting function remains the same) ...

# 2. CONFIGURE TRAINER
trainer = SFTTrainer(
    model = model,
    tokenizer = tokenizer,
    train_dataset = train_dataset,
    eval_dataset = eval_dataset,

    # --- FIX: Add this line ---
    formatting_func = formatting_prompts_func,

    # Tell it to load the 'text' field generated by the function
    dataset_text_field = "text",

    max_seq_length = 2048,
    args = SFTConfig(
        per_device_train_batch_size = 2,
        gradient_accumulation_steps = 4,
        # max_steps = 100,
        # ADD this to train for exactly 1 full pass
        num_train_epochs = 2,
        learning_rate = 2e-4,
        fp16 = not torch.cuda.is_bf16_supported(),
        bf16 = torch.cuda.is_bf16_supported(),
        logging_steps = 1,
        eval_strategy = "steps",
        eval_steps = 10,
        output_dir = output_dir,
    ),
)

# 3. TRAIN
trainer_stats = trainer.train()

# Runs evaluation on the 'eval_dataset' we defined earlier
test_results = trainer.evaluate()
print(f"Final Test Loss: {test_results['eval_loss']}")


import pandas as pd
import matplotlib.pyplot as plt

# Extract logs
history = trainer.state.log_history
df = pd.DataFrame(history)

# Separate training and validation metrics
train_loss = df[df['loss'].notna()][['step', 'loss']]
val_loss = df[df['eval_loss'].notna()][['step', 'eval_loss']]

# Plot
plt.figure(figsize=(10, 5))
plt.plot(train_loss['step'], train_loss['loss'], label='Training Loss')
plt.plot(val_loss['step'], val_loss['eval_loss'], label='Validation Loss', color='red')
plt.xlabel('Steps')
plt.ylabel('Loss')
plt.title('Training vs Validation Loss')
plt.legend()
plt.show()